/* Contorn: mateixa màscara que el sprite masked, tinta uniforme (silueta). */
static void title_logo_silhouette_pen(u8* v, u8 x0, u8 y0, u8 pen) {
    const u8* s = title_logo_masked;
    u8 row, col;
    for(row = 0; row < TITLE_LOGO_H; ++row) {
        u8* d = cpct_getScreenPtr(v, x0, (u8)(y0 + row));
        for(col = 0; col < TITLE_LOGO_W_BYTES; ++col) {
            u8 m = *s++;
            (void)*s++; /* color original; només usem màscara */
            *d = (u8)((*d & m) | pen);
            ++d;
        }
    }
}

