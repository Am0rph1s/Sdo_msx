static void buildGlyph(const u8* bits, u8* out, u8 pen){
    u8 y,i=0;
    for(y=0;y<8;++y){
        u8 row=(u8)(bits[y]&0x0Fu);
        u8 p0=(row&8)?pen:0,p1=(row&4)?pen:0,p2=(row&2)?pen:0,p3=(row&1)?pen:0;
        out[i++]=cpct_px2byteM0(p0,p1);
        out[i++]=cpct_px2byteM0(p2,p3);
    }
}

static u8 charToIdx(char c){
    if(c>='A'&&c<='Z')return(u8)(1+(c-'A'));
    if(c>='0'&&c<='9')return(u8)(27+(c-'0'));
    return 0;
}

void hud_buildFont(void){
    u8 i;
    for(i=0;i<37;++i)buildGlyph(font_bits[i],glyphs[i],4);
}

const u8* hud_getGlyph(char c){
    return glyphs[charToIdx(c)];
}

const u8* hud_getGlyphHi(char c){
    buildGlyph(font_bits[charToIdx(c)],g_hi_tmp,15);
    return g_hi_tmp;
}

void hud_updateScoreText(u16 score, char* buffer){
    static const u16 pow10hi[4]={10000u,1000u,100u,10u};
    u8 i,d;
    buffer[0]='S'; buffer[1]='C'; buffer[2]='O'; buffer[3]='R';
    buffer[4]='E'; buffer[5]=' ';
    for(i=0;i<4u;++i){
        d=0;
        while(score>=pow10hi[i]){score=(u16)(score-pow10hi[i]);++d;}
        buffer[6+i]=(char)('0'+d);
    }
    buffer[10]=(char)('0'+(u8)score);
    buffer[11]=0;
}

void hud_updateLevelText(u8 lv, char* buffer){
    u8 tens,ones;
    if(lv<1u)lv=1u;
    else if(lv>25u)lv=25u;
    buffer[0]='L';
    buffer[1]='V';
    if(lv>=20u){tens=2u;ones=(u8)(lv-20u);}
    else if(lv>=10u){tens=1u;ones=(u8)(lv-10u);}
    else{tens=0u;ones=lv;}
    buffer[2]=(char)('0'+tens);
    buffer[3]=(char)('0'+ones);
    buffer[4]=0;
}

void hud_updateLivesText(u8 lives, char* buffer){
    buffer[0]='L'; buffer[1]='I'; buffer[2]='V';
    buffer[3]='E'; buffer[4]='S'; buffer[5]=' ';
    if(lives>9)lives=9;
    buffer[6]='0'+lives;
    buffer[7]=0;
}
