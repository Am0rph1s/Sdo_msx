# Extract MSX sprite patterns from nau_dx.c to PNG files
# Each sprite is 16x16 (or 8x8) in MSX Screen 2 format

Add-Type -AssemblyName System.Drawing

function Convert-SpritePatternToPng {
    param(
        [string]$Name,
        [byte[]]$Pattern,
        [int]$Width = 16,
        [int]$Height = 16,
        [string]$OutputDir
    )

    $bmp = New-Object System.Drawing.Bitmap($Width, $Height)

    if ($Width -eq 8 -and $Height -eq 8) {
        # 8x8 sprite: 1 byte per row, 8 rows
        for ($row = 0; $row -lt 8; $row++) {
            $b = $Pattern[$row]
            for ($bit = 0; $bit -lt 8; $bit++) {
                if (($b -band (1 -shl (7 - $bit))) -ne 0) {
                    $bmp.SetPixel($bit, $row, [System.Drawing.Color]::White)
                } else {
                    $bmp.SetPixel($bit, $row, [System.Drawing.Color]::Black)
                }
            }
        }
    } elseif ($Width -eq 16 -and $Height -eq 16) {
        # 16x16 sprite: MSX format
        # First 16 bytes = left half (pixels 0-7), next 16 = right half (pixels 8-15)
        for ($row = 0; $row -lt 16; $row++) {
            $leftByte = $Pattern[$row]
            $rightByte = $Pattern[$row + 16]
            for ($bit = 0; $bit -lt 8; $bit++) {
                if (($leftByte -band (1 -shl (7 - $bit))) -ne 0) {
                    $bmp.SetPixel($bit, $row, [System.Drawing.Color]::White)
                } else {
                    $bmp.SetPixel($bit, $row, [System.Drawing.Color]::Black)
                }
                if (($rightByte -band (1 -shl (7 - $bit))) -ne 0) {
                    $bmp.SetPixel($bit + 8, $row, [System.Drawing.Color]::White)
                } else {
                    $bmp.SetPixel($bit + 8, $row, [System.Drawing.Color]::Black)
                }
            }
        }
    }

    $path = Join-Path $OutputDir "$Name.png"
    $bmp.Save($path, [System.Drawing.Imaging.ImageFormat]::Png)
    $bmp.Dispose()
    Write-Host "  $path ($Width x $Height)"
}

function Convert-OverlayToPng {
    param(
        [string]$Name,
        [byte[]]$WhitePattern,
        [byte[]]$RedPattern,
        [string]$OutputDir
    )

    $bmp = New-Object System.Drawing.Bitmap(16, 16)

    for ($row = 0; $row -lt 16; $row++) {
        $wb = $WhitePattern[$row]
        $rb = $WhitePattern[$row + 16]
        $wrb = $RedPattern[$row]
        $rrb = $RedPattern[$row + 16]
        for ($bit = 0; $bit -lt 8; $bit++) {
            $x = $bit
            $wSet = ($wb -band (1 -shl (7 - $bit))) -ne 0
            $wrSet = ($wrb -band (1 -shl (7 - $bit))) -ne 0
            if ($wrSet) { $bmp.SetPixel($x, $row, [System.Drawing.Color]::Red) }
            elseif ($wSet) { $bmp.SetPixel($x, $row, [System.Drawing.Color]::White) }
            else { $bmp.SetPixel($x, $row, [System.Drawing.Color]::Black) }

            $x2 = $bit + 8
            $wSet2 = ($rb -band (1 -shl (7 - $bit))) -ne 0
            $wrSet2 = ($rrb -band (1 -shl (7 - $bit))) -ne 0
            if ($wrSet2) { $bmp.SetPixel($x2, $row, [System.Drawing.Color]::Red) }
            elseif ($wSet2) { $bmp.SetPixel($x2, $row, [System.Drawing.Color]::White) }
            else { $bmp.SetPixel($x2, $row, [System.Drawing.Color]::Black) }
        }
    }

    $path = Join-Path $OutputDir "$Name.png"
    $bmp.Save($path, [System.Drawing.Imaging.ImageFormat]::Png)
    $bmp.Dispose()
    Write-Host "  $path (16x16, white+red overlay)"
}

